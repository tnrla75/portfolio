package travel_package;

public class RouteDTO {
	String routeNum;
	String id;
	String routeTitle;
	String routeDate;
	String routeContent;
	String routePhoto;
	String route;
	public String getRouteNum() {
		return routeNum;
	}
	public void setRouteNum(String routeNum) {
		this.routeNum = routeNum;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getRouteTitle() {
		return routeTitle;
	}
	public void setRouteTitle(String routeTitle) {
		this.routeTitle = routeTitle;
	}
	public String getRouteDate() {
		return routeDate;
	}
	public void setRouteDate(String routeDate) {
		this.routeDate = routeDate;
	}
	public String getRouteContent() {
		return routeContent;
	}
	public void setRouteContent(String routeContent) {
		this.routeContent = routeContent;
	}
	public String getRoutePhoto() {
		return routePhoto;
	}
	public void setRoutePhoto(String routePhoto) {
		this.routePhoto = routePhoto;
	}
	public String getRoute() {
		return route;
	}
	public void setRoute(String route) {
		this.route = route;
	}
	
}
