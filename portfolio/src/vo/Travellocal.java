package vo;

public class Travellocal {
	int local_num;
	String local_name;
	String local_nation;
	String local_content;
	String local_img;
	public int getLocal_num() {
		return local_num;
	}
	public void setLocal_num(int local_num) {
		this.local_num = local_num;
	}
	public String getLocal_name() {
		return local_name;
	}
	public void setLocal_name(String local_name) {
		this.local_name = local_name;
	}
	public String getLocal_nation() {
		return local_nation;
	}
	public void setLocal_nation(String local_nation) {
		this.local_nation = local_nation;
	}
	public String getLocal_content() {
		return local_content;
	}
	public void setLocal_content(String local_content) {
		this.local_content = local_content;
	}
	public String getLocal_img() {
		return local_img;
	}
	public void setLocal_img(String local_img) {
		this.local_img = local_img;
	}
	
	
}
